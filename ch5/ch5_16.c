#include <stdio.h>
int main()
{
    char diamond[7][7] = {{' ', ' ', ' ', '*'}, {' ', ' ', '*', '*', '*'}, {' ', '*', '*', '*', '*', '*'}, {'*', '*', '*', '*', '*', '*', '*'}, {' ', '*', '*', '*', '*', '*'}, {' ', ' ', '*', '*', '*'}, {' ', ' ', ' ', '*'}};
    int i, j;
    for (i = 1; i <= 7; i++)
    {
        for (j = 1; j <= 7; j++)
            printf("%c", diamond[i][j]);
    }
    printf("\n");
    return 0;
}